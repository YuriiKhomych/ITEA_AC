first_list = [2, 3, 4, 5]
second_list = [9, 8, 7, 6]

# 5 cond
def accepts_list(first_list, second_list):
    return list(zip(first_list, second_list))
print(accepts_list(first_list, second_list))


# 6 cond

def generator_squares(first_list, second_list):
    result = []
    result.append(list(i ** g for i in first_list for g in second_list))
    return result

print(generator_squares(first_list, second_list))

